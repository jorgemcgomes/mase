package mase.util;

/**
 * Point with weight
 */
public class WeightedPoint {
    private Point point;
    private double weight;

    public Point getPoint() {
        return point;
    }

    public void setPoint(Point point) {
        this.point = point;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
    
}
